// OPERASI PERBANDINGAN
/*
  >    -----> Lebi Dari
  <    -----> Kurang Dari
  >=   -----> Lebi Dari Sama Dengan
  <=   -----> Kurang Dari Sama Dengan
  ==   -----> Sama Dengan
  !=   -----> Tdak Sama Dengan
* */
fun main() {
    val result: Boolean = 100 < 200
    println(result)

    println(300 <= 100)
    println(10 >= 5)

    println("a" < "b")
}

